void PrintMatrix(int[,] matrix)
{
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            if (matrix[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void PaintImage(int row, int columns)
{
    if (pic[row, columns] == 0)
    {
        pic[row, columns] = 1;
        PaintImage(row - 1, columns);
        PaintImage(row, columns - 1);
        PaintImage(row + 1, columns);
        PaintImage(row, columns + 1);
    }
}

